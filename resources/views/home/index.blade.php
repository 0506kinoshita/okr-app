・objective 
ワクワクするような目標
key_result（定量化）
これを達成できたらobjectiveが達成されたと言って良いというものを3つ程

・例）
objective）日本一のラーメン屋を作りたい
key_result）・売り上げ2000万・従業員の応募が月50人・口コミサイトで4.0以上の評価

・自信度）
10「絶対達成できる」
5「ベスト尽くせばいけるかも」
1「無理でしょ。。。」

